package main

import (
	"fmt"
	"net/http"

	"github.com/JJDoneAway/addressbook/controllers"
	"github.com/JJDoneAway/addressbook/helper"

	docs "github.com/JJDoneAway/addressbook/docs" // docs is generated by Swag CLI, you have to import it.
	httpSwagger "github.com/swaggo/http-swagger"
)

var port = "8080"

// @title           GO Example Addressbook
// @version         1.0
// @description     This is a simple GO application with some basic REST CRUD operations.

// @Accept json
// @Produce json

// @contact.name   Johannes HÃ¶hne
// @contact.email  Johannes.Hoehne1@mail.schwarz

// @license.name  Apache 2.0
// @license.url   http://www.apache.org/licenses/LICENSE-2.0.html
func main() {
	fmt.Println("Register front controllers...")

	helper.AddDummies()

	//register swagger UI
	docs.SwaggerInfo.BasePath = "/"
	http.Handle("/swagger/", httpSwagger.Handler(
		httpSwagger.URL("/swagger/doc.json"), //The url pointing to API definition
	))
	http.HandleFunc("/", func(w http.ResponseWriter, r *http.Request) {
		http.Redirect(w, r, "/swagger/index.html", http.StatusMovedPermanently)
	})

	//register user api
	controllers.RegisterControllers()

	//start server
	fmt.Println("Start http server on port", port)
	err := http.ListenAndServe(":"+port, nil)
	fmt.Print("Waiting for requests...")
	if err != nil {
		fmt.Print(err)
	}

}
